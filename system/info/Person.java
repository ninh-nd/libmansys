package system.info;
import java.util.*;

public class Person {

	private String name;

	private String email;

	private String phoneNumber;

	private String address;

	public Person(String name, String email, String phoneNumber, String address) {
		this.name = name;
		this.email = email;
		this.phoneNumber = phoneNumber;
		this.address = address;
	}

}
